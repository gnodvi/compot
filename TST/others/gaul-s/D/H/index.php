<p>GAUL is still under heavy development, and therefore is unlikely to be bug free.  If you find a bug, please send details of your system, the version of GAUL that you are using and concise instructions for reproducing the crash, or erronerous result, to <a href="mailto:gaul-devel@lists.sourceforge.net?Subject=GAUL Bug report">the GAUL mailing list</a>.  Alternatively, use SourceForge's bug submission facility.</p>
<p>If there is a particular feature that you would like to see added to GAUL, you are welcome to send details to <a href="mailto:gaul-devel@lists.sourceforge.net?Subject=GAUL feature request">the GAUL mailing list</a>.  If you would like to discuss terms for a proprietry modification, enhancement or addition, please send your request to <a href="mailto:gaul@linux-domain.com">gaul@linux-domain.com</a>.</p>
