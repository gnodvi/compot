<p><a NAME="introduction"></a></p>
<table BORDER=0 CELLSPACING=0 CELLPADDING=2 WIDTH="100%">
  <tr>
  <td ALIGN=CENTER BGCOLOR="#2222AA"><b><font face="Arial,Helvetica" color="#FFFFFF">Introduction</font></b></td>
  </tr>
</table>

<table BORDER=0 CELLSPACING=0 CELLPADDING=0 WIDTH="100%">
<tr>
<td VALIGN=TOP>
<p>The Genetic Algorithm Utility Library (or, GAUL for short) is a flexible programming library designed to aid in the development of applications that use genetic, or evolutionary, algorithms.  It provides data structures and functions for handling and manipulation of the data required for serial and parallel evolutionary algorithms.  Additional stochastic algorithms are provided for comparison to the genetic algorithms.  Much of the functionality is also available through a simple <a href="http://www.s-lang.org">S-Lang</a> interface.</p>
<p>
<ul>
  <li>Darwinian, Lamarckian or Baldwinian evolutionary schemes.</li>
  <li>Both steady-state and generation-based GAs included.</li>
  <li>The island model of evolution is available.</li>
  <li>Chromosome datatype agnostic.  A selection of common chromosome types are built-in.</li>
  <li>Includes most of the usual crossover, mutation and selection operators.</li>
  <li>Allows user-defined crossover, mutation, selection, adaptation and replacement operators.</li>
  <li>Availability of multiple chromosome genomes.</li>
  <li>Full support for multiple, simultaneously evolved, populations.</li>
  <li>Choice of high-level or low-level interface functions.</li>
  <li>Additional optimisation algorithms are built-in for local optimisation or comparative purposes:
      <ul><li>Hill-climbing.</li>
          <li>Tabu Search.</li>
          <li>Simulated Annealling.</li>
          <li>Simplex Search.</li>
          <li>Steepest Ascents.</li>
          <li>Deterministic Crowding.</li>
          <li>Differential Evolution.</li>
      </ul></li>
  <li>Trivial to extend using external code via the built-in code hooks.</li>
  <li>May be driven by, or extended by, simple yet powerful <a href="http://www.s-lang.org">S-Lang</a> scripts.</li>
  <li>Support for multiprocessor calculations on various architectures:</li>
      <ul><li>MPI</li>
          <li>OpenMP 2.0</li>
          <li>pthreads</li>
          <li>forked-process model</li>
      </ul></li>
  <li>Written using highly portable C code.</li>
  <li>Distributed with a large set of example programs.</li>
  <li>Available as a free download, with full source code.</li>
</ul>
<p>The current primary aims of this project are to produce documentation, examples and test cases for this open-source release of GAUL.  Several new features are also planned for GAUL including wrappers to enable its use with programming languages other than C/C++.</p>
<p>The main development platform for GAUL is Linux with the gcc compiler, but it should be fairly trivial to use GAUL on any POSIX compliant systems.  Full use is made of the GNU maketools to aid portability issues.  <a href="platforms.html">Here</a> is a list of platforms that are known to work.  Recent versions of GAUL have been successfully used a variety of operating systems, including RedHat Linux, FreeBSD, Sun's Solaris and SGI's IRIX.  Additionally, GAUL may be compiled successfully on Microsoft Windows, albeit with a few minor features disabled.</p>
