<p>The GAUL project is a member of the <a href="http://foundry.ai-depot.com/">AI foundry</a>.</p>
<p>Press releases: <a href="http://mccammon.ucsd.edu/~adcock/PRESS_RELEASE_030827.txt">27 August, 2003</a>, <a href="http://mccammon.ucsd.edu/~adcock/PRESS_RELEASE_040628.txt">28 June, 2004</a>, <a href="PRESSRELEASE050422.txt">22 April, 2005</a>.</p>
