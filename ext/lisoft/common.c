/*--------------------------------YInt--------------------------------------*/ 
int 
YInt (float f) 
{ 
  int     i; 
  float   o; 
 
  i = (int) f; 
  o = f - i; 
  if      (o > 0.5)   i++; 
  else if (o < -0.5)  i--; 
 
  return (i); 
} 
